        public void Withdraw_ShouldDecreaseBalance(decimal initialBalance, decimal value, decimal expectedBalance)
        {
            //Arrange
            var account = new BankAccount("321", initialBalance);

            //Act
            account.Withdraw(value);

            //Assert
            Assert.True(account.Balance == expectedBalance);
        }

